-- (2) Property Address data gets populated
-- For example If "ParcelID = 1" and its "PropertyAddress = 508 Mathes CT", and if theres another "ParcelID = 1" but it doesn't have "PropertyAddress", then we can use the first "ParcelId = 1"'s PropertyAddress on the second one by populating it.
-- We need to do self join so that the same coloumn can look at it self
-- ISNULL - if the "a.PropertyAddress" is null then we want to put "b.PropertyAddress" value in there

Select *
From PortfolioProject.dbo.NashvilleHousing
order by ParcelID

Select a.ParcelID, a.PropertyAddress, b.ParcelID, b.PropertyAddress, ISNULL(a.PropertyAddress,b.PropertyAddress)
From PortfolioProject.dbo.NashvilleHousing a
JOIN PortfolioProject.dbo.NashvilleHousing b
	on a.ParcelID = b.ParcelID
	AND a.[UniqueID ] <> b.[UniqueID ]
Where a.PropertyAddress is null

Update a
SET PropertyAddress = ISNULL(a.PropertyAddress,b.PropertyAddress)
From PortfolioProject.dbo.NashvilleHousing a
JOIN PortfolioProject.dbo.NashvilleHousing b
	on a.ParcelID = b.ParcelID
	AND a.[UniqueID ] <> b.[UniqueID ]
Where a.PropertyAddress is null


